/*
 * Bubble sort
 *
 * O(n^2)
 *
 */

#include <stdio.h>
#include "../helpers.h"

/* -----------------------------------------------------------------------------
 * Algorithm
 * -------------------------------------------------------------------------- */

void bubble(int A[], int s, int n) {
  int i, j, temp;
  for (i = s; i < n; i++) {
    for (j = n; j > i; j--) {
      if (A[j] < A[j - 1]) {
        temp = A[j - 1];
        A[j - 1] = A[j];
        A[j] = temp;
      }
    }
  }
}

/* -----------------------------------------------------------------------------
 * Tests
 * -------------------------------------------------------------------------- */

void tests() {
  int A[8] = {4, 1, 32, 12, 17, 5, 1, 13};
  bubble(A, 0, 7);
  assert(is_sorted(A, 0, 7));
  print_array(A, 0, 7);
}

int main() {
  tests();

  return 0;
}
