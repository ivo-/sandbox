%% Member

%% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% Solution
%% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

member([X|_], X).
member([_|Q], X) :- member(Q,X).

%% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% Tests
%% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

?- member([1,2,3,4], 2).
?- member([1,2,[3],4], [3]).

?- member([X],3), writeln(X).
?- member([1,2,4|X],3), writeln(X).
?- member([1,2,[3],4], X), writeln(X).
