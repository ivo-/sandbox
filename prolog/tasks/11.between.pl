%% Between

%% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% Solution
%% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

%% between(X,Y,Z) - Z is between X and Y
between(X,Y,X) :- X =< Y.
between(X,Y,Z) :- X < Y, K is X + 1, between(K,Y,Z).

%% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% Tests
%% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

?- between(1,10,1).
?- between(1,10,10).
?- between(1,10,7).
?- not(between(1,10,17)).
